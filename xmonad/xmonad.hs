-- Now run xmonad with all the defaults we set up.

-- Run xmonad with the settings you specify. No need to modify this.
--
myXmonadBar = "dzen2 -x '1440' -y '0' -h '24'  -w '1280' -ta 'l' -fg '#FFFFFF' -bg '#1B1D1E'"
myStatusBar = "i3status -c " ++ myConfigDir ++ "/i3status.conf | dzen2 -x '2080' -w '1280'  -h '24' -ta 'r' -bg '#1B1D1E' -fg '#FFFFFF' -y '24'"
myConfigDir = "$HOME/.xmonad/"
myBitmapsDir = "$HOME/.xmonad/dzen2"

main = do
  myDzen <- spawnPipe myXmonadBar
  dzenRightBar <- spawnPipe myStatusBar
  xmonad defaultConfig {
      -- simple stuff
        terminal           = myTerminal,
        focusFollowsMouse  = myFocusFollowsMouse,
        clickJustFocuses   = myClickJustFocuses,
        borderWidth        = myBorderWidth,
        modMask            = myModMask,
        workspaces         = myWorkspaces,
        normalBorderColor  = myNormalBorderColor,
        focusedBorderColor = myFocusedBorderColor,

      -- key bindings
        keys               = myKeys,
        mouseBindings      = myMouseBindings,

      -- hooks, layouts
        layoutHook         = myLayout,
        manageHook         = myManageHook,
        handleEventHook    = myEventHook,
        --logHook            = myLogHook dzenLeftBar >> fadeInactiveLogHook 0xdddddddd,
        logHook            = dynamicLogWithPP $ byorgeyPP { ppOutput = hPutStrLn myDzen,
          ppExtras = [wrapL "bat: " "" battery, aumixVolume, date "%c"] },
        startupHook        = myStartupHook
    }
